# Create a list of fruits
fruits_list = ['strawberry', 'apple', 'orange', 'kiwi']

# Iterate over each item in the fruit list
for fruit in fruits_list:
    if 'a' in fruit:
        print(f'The letter "a" is present in the {fruit}') 
    else:
        print(f'There is no "a" present in the {fruit}')
# Check if the letter 'a' is present in the list