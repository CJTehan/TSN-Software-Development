# Code with addition
added = 12 + 8
print(added)

# Code with subtraction
subtracted = 20 - 6
print(subtracted)

# Code with multiplication
multiplied = 7 * 4
print(multiplied)

# code with division
divided = 5 / 2
print(divided)

# code with modulo (remainder after dividing by)
modulo = 18 % 4
print(modulo)

# Code with floor division (dividing but giving an integer)
division = 10 // 3
print(division)
print(f'10 divided by 3 is {division} remainder {modulo}')

# Code with exponent operator
exponent = 2 ** 2
print(exponent)